package uk.ac.cam.cl.dtg.android.time.data.quadtree;

public class QuadTreeException extends Exception {

	private static final long serialVersionUID = -3392527441627787789L;

	public QuadTreeException() {
	}

	public QuadTreeException(String message) {
		super(message);
	}

	public QuadTreeException(Throwable cause) {
		super(cause);
	}

	public QuadTreeException(String message, Throwable cause) {
		super(message, cause);
	}

}
